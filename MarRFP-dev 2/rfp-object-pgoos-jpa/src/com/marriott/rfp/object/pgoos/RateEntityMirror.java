package com.marriott.rfp.object.pgoos;

public class RateEntityMirror {
	
	private String parentRateOfferId;
	private String parentPricingType;
	private String parentPricingTypeId;
	private String parentRateEntityId;
	private String parentPriority;
	private String parentRoomPool;

	public RateEntityMirror() {}

	public String getParentRateOfferId() {
		return parentRateOfferId;
	}

	public void setParentRateOfferId(String parentRateOfferId) {
		this.parentRateOfferId = parentRateOfferId;
	}

	public String getParentPricingType() {
		return parentPricingType;
	}

	public void setParentPricingType(String parentPricingType) {
		this.parentPricingType = parentPricingType;
	}

	public String getParentPricingTypeId() {
		return parentPricingTypeId;
	}

	public void setParentPricingTypeId(String parentPricingTypeId) {
		this.parentPricingTypeId = parentPricingTypeId;
	}

	public String getParentRateEntityId() {
		return parentRateEntityId;
	}

	public void setParentRateEntityId(String parentRateEntityId) {
		this.parentRateEntityId = parentRateEntityId;
	}

	public String getParentPriority() {
		return parentPriority;
	}

	public void setParentPriority(String parentPriority) {
		this.parentPriority = parentPriority;
	}

	public String getParentRoomPool() {
		return parentRoomPool;
	}

	public void setParentRoomPool(String parentRoomPool) {
		this.parentRoomPool = parentRoomPool;
	}
}